# 算数运算符 study

print(10 + 20)

print(10 - 20)

print(10 * 20)

print(10 / 20) 
print(10/3)#注意有小数位 3.3333333333333335

# 取整数
print(70 // 20)

# 取余数
print(10 % 20)

# 幂
print(2 ** 3)

# 字符串  *10 表示 拼接字符串10次 下面是拼接10 个我
print("我" * 10)

print("对不起" * 100)

print(7*2**3/4%3)

# 运算符 优先级  幂> * / % // > = -
#  可以使用 （）提高运算符的优先级
