# for循环遍历　迭代　iteration 遍历
name_list = ["大海", "小海", "大周", "小黄", "大林"]

# 使用迭代遍历列表
for name in name_list:
    print("我的名字是：%s" % name)
# 列表/的遍历
address_list = []
address_list = address_list.__add__([12, "aa"])
address_list.append("周")
address_list.append("大")
address_list.append("福")

for addre in address_list:
    print(type(addre))
    print(addre)
