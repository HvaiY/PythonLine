name_list = ["zhangsan", "lisi", "wangwu"]
num_list = [123, 34, 56]

#  升序
name_list.sort()
num_list.sort()
print(name_list)
print(num_list)

# 降序
name_list.sort(reverse=True)
num_list.sort(reverse=True)
print(name_list)
print(num_list)

# 逆序　反转
name_list.reverse()
num_list.reverse()
print(name_list)
print(num_list)
